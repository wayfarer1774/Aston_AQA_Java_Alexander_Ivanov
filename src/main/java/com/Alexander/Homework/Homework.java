    public static void secondMethod(int a) {
        if (a >= 0) {
            System.out.println("Число является положительным");
        } else {
            System.out.println("Число является отрицательным");
        }
    }

    public static boolean thirdMethod(int a) {
        if (a < 0) {
            return true;
        } else {
            return false;
        }
    }

    public static void fourthMethod(String a, int b) {
        for (; b > 0; b--) {
            System.out.println(a);
        }
    }

    public static boolean fifthMethod(int a) {
        if ((a % 4 == 0) && (a % 100 != 0) || (a % 400 == 0)) {
            return true;
        } else {
            return false;
        }
    }

    public static int[] tenthMethod(int len, int initialValue) {
        int[] arr = new int[len];
        for (int i = 0; i < len; i++) {
            arr[i] = initialValue;
        }
        return arr;
    }
