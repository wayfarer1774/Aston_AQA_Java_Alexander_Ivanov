        fifthMethod(100);

        //6-ое задание
        int[] arr = {1, 1, 0, 0, 1, 0, 1, 1, 0, 0};
        int l = arr.length;
        for (int i = 0; i < l; i++) {
            if (arr[i] == 0) {
                arr[i] = 1;
            } else {
                arr[i] = 0;
            }
        }

        //7-ое задание
        int[] arr2 = new int[100];
        for (int i = 0; i < 100; i++) {
            int j = i + 1;
            arr2[i] = j;
        }

        //8-ое задание
        int[] arr3 = {1, 5, 3, 2, 11, 4, 5, 2, 4, 8, 9, 1};
